        // 遍历筛选出来的支部，统计各支部的党员数量和委员组成
        $partybranchId = C('ROLE_PARTY_ID');
        foreach ($page['list'] as $index => $item) {
            // 统计各支部党员数量 
            $itemid = $item['id'];
