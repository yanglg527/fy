    public function  ajaxSaveUserStatus()
    {
        $id = I('id');
        $user = D('User')->find($id);
        if ($user) {
            $status = $user['status'];
            if ($status == 1) {
                $user['status'] = 0;
            } else {
                $user['status'] = 1;
            }
            $user['update_time'] = time();
            D('User')->where(array('uid' => $id))->save($user);
            ajaxMsg(0, '保存成功');
        } else {
            ajaxMsg(1, '该用户已被删除了');
        }

    }


