			$item['total_count'] = D('User')->where("branch_id=$itemid")->count();
            $count = D('User')->where("branch_id=$itemid and role_id=$partybranchId and status=1")->count();
            $total_point = D('User')->where("branch_id=$itemid and status=1")->sum('score');
